let number=Math.floor(Math.random()*10);
console.log("Your a Number is : "+number);
switch(number)
{
    case 0:
        console.log("Zero")
    break;
    case 1:
        console.log("ONE")
    break;
    case 2:
        console.log("TWO")
    break;
    case 3:
        console.log("THREE")
    break;
    case 4:
        console.log("FOUR")
    break;
    case 5:
        console.log("FIVE")
    break;
    case 6:
        console.log("SIX")
    break;
    case 7:
        console.log("SEVEN")
    break;
    case 8:
        console.log("EIGHT")
    break;
    case 9:
        console.log("Nine")
    break;
    default :
    console.log("Enter a Valid one Digit Number ")
}